const I18nContext = createContext({
  locale: 'en',
  t: (key, fallback = key) => fallback,
})

function getByPath(obj, path) {
  return path.split('.').reduce((acc, part) => acc?.[part], obj)
}

function detectBrowserLocale() {
  if (typeof navigator === 'undefined') return 'en'

  const candidates = Array.isArray(navigator.languages) && navigator.languages.length
    ? navigator.languages
    : [navigator.language || 'en']

  for (const candidate of candidates) {
    const language = String(candidate || '').toLowerCase()
    if (language.startsWith('fr')) return 'fr'
    if (language.startsWith('es')) return 'es'
    if (language.startsWith('en')) return 'en'
  }

  return 'en'
}

export function resolveSiteLocale(preference = 'auto', browserLocale = detectBrowserLocale()) {
  if (preference && preference !== 'auto' && translations[preference]) return preference
  return browserLocale
}

export function getOptionLabel(option, locale) {
  return option.labels[locale] || option.labels.en
}

export function getModuleCopy(type, locale, fallback = {}) {
  const translated = MODULE_TRANSLATIONS[locale]?.[type] || MODULE_TRANSLATIONS.en?.[type]
  return {
    name: translated?.name || fallback.name || type,
    description: translated?.description || fallback.description || '',
  }
}

export function getModuleCategoryLabel(category, locale) {
  return extraTranslations[locale]?.serverDetail?.categories?.[category]
    || extraTranslations.en?.serverDetail?.categories?.[category]
    || category
}

export function I18nProvider({ children }) {
  const siteLanguage = useAuthStore((state) => state.user?.site_language)
  const [browserLocale, setBrowserLocale] = useState(() => detectBrowserLocale())
  const locale = useMemo(() => resolveSiteLocale(siteLanguage, browserLocale), [siteLanguage, browserLocale])

  useEffect(() => {
    if (typeof window === 'undefined') return undefined

    const syncBrowserLocale = () => setBrowserLocale(detectBrowserLocale())
    syncBrowserLocale()
    window.addEventListener('languagechange', syncBrowserLocale)

    return () => window.removeEventListener('languagechange', syncBrowserLocale)
  }, [])

  useEffect(() => {
    document.documentElement.lang = locale
  }, [locale])

  const value = useMemo(() => ({
    locale,
    t: (key, fallback = key) => {
      const translated = getByPath(translations[locale], key)
      if (translated !== undefined) return translated
      const english = getByPath(translations.en, key)
      return english !== undefined ? english : fallback
    },
  }), [locale])

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>
}

export function useI18n() {
  return useContext(I18nContext)
}
